testList = ['ford', 'chevy', 'tesla', 'toyota','nissan']

#Testing to see if first element from list will be removed using remove method
remList = testList.remove('ford')
print(f"\nThis statement is testing if the the first element 'ford' was removed using 'remove method'\t {remList} ")


#Testing to see if last element from list will be removed using pop method
